        function object = frameConversion( timeVector, scale )
            isGps = false;
            if strcmpi(scale, 'gps')
                isGps = true;
            end
            year = timeVector(1);
            month = timeVector(2);
            day = timeVector(3);
            hour = timeVector(4);
            minute = timeVector(5);
            second = timeVector(6);
            MJD_UTC = object.Mjday(year, month, day, hour, minute, second, isGps);
            eopData = object.getRecentEop();
            [U, dU] = object.getUdU( eopData, MJD_UTC);
            object.U = U;
            object.dU = dU;
        end
        function [positionOut, velocityOut, accelerationOut] = ...
                convert(obj, positionIn, velocityIn, accelerationIn, from, to)
            isEciToEcef = strcmpi(from, 'eci') && strcmpi(to, 'ecef');
            isEcefToEci = strcmpi(to, 'eci') && strcmpi(from, 'ecef');
            accelerationOut = accelerationIn;
            if ~(isEciToEcef || isEcefToEci)
                positionOut = positionIn;
                velocityOut = velocityIn;
                return;
            end
            if isEcefToEci
                % Transformation from WGS to ICRS
                positionOut = obj.U'*positionIn';
                velocityOut = obj.U'*velocityIn' + obj.dU'*positionIn';
            end
            if isEciToEcef
                % Transformation from ICRS to WGS
                positionOut = obj.U*positionIn';
                velocityOut = obj.U*velocityIn' + obj.dU*positionIn';
            end
        end

        function [U, dU] = getUdU(obj, eopdata, MJD_UTC)
            [x_pole,y_pole,UT1_UTC,LOD,dpsi,deps,dx_pole,dy_pole,TAI_UTC] = obj.IERS(eopdata,MJD_UTC);%#ok
            [UT1_TAI,UTC_GPS,UT1_GPS,TT_UTC,GPS_UTC] = obj.timediff(UT1_UTC,TAI_UTC);%#ok
            MJD_UT1 = MJD_UTC + UT1_UTC/86400;
            MJD_TT = MJD_UTC + TT_UTC/86400;
            % Form bias-precession-nutation matrix
            NPB = obj.iauPnm06a(obj.constDJM0, MJD_TT);
            % Form Earth rotation matrix
            Theta = obj.iauRz( obj.iauGst06(obj.constDJM0, MJD_UT1, obj.constDJM0, MJD_TT, NPB),eye(3) );
            % Polar motion matrix (TIRS->ITRS, IERS 2003)
            Pi = obj.iauPom00(x_pole, y_pole, obj.iauSp00(obj.constDJM0, MJD_TT));
            % ICRS to ITRS transformation matrix and derivative
            S = zeros(3);
            S(1,2) = 1;
            S(2,1) = -1;              			 % Derivative of Earth rotation
            % Omega  = 7292115.8553e-11+4.3e-15*( (MJD_UTC-const.MJD_J2000)/36525 ); % [rad/s]
            Omega = obj.constomega_Earth-0.843994809*1e-9*LOD;  % IERS
            dTheta = Omega*S*Theta;           				 % matrix [1/s]
            U      = Pi*Theta*NPB;                			 % ICRS to ITRS transformation
            dU     = Pi*dTheta*NPB;               			 % Derivative [1/s]
        end

        function w = iauAnp(obj, a)
            w = rem(a, obj.constD2PI);
            if (w < 0)
                w = w + obj.constD2PI;
            end
        end

        function [x, y] = iauBpn2xy(~, rbpn)
            % Extract the X,Y coordinates.
            x = rbpn(3,1);
            y = rbpn(3,2);
        end

        function eo = iauEors(~, rnpb, s)
            % Evaluate Wallace & Capitaine (2006) expression (16).
            x = rnpb(3,1);
            ax = x / (1.0 + rnpb(3,3));
            xs = 1.0 - ax * x;
            ys = -ax * rnpb(3,2);
            zs = -x;
            p = rnpb(1,1) * xs + rnpb(1,2) * ys + rnpb(1,3) * zs;
            q = rnpb(2,1) * xs + rnpb(2,2) * ys + rnpb(2,3) * zs;
            if ((p ~= 0) || (q ~= 0))
                eo = s - atan2(q, p);
            else
                eo = s;
            end
        end

        function theta = iauEra00(obj, dj1, dj2)
            % Days since fundamental epoch.
            if (dj1 < dj2)
                d1 = dj1;
                d2 = dj2;
            else
                d1 = dj2;
                d2 = dj1;
            end
            t = d1 + (d2- obj.constDJ00);
            % Fractional part of T (days).
            f = rem(d1, 1.0) + rem(d2, 1.0);
            % Earth rotation angle at this UT1.
            theta = obj.iauAnp(obj.constD2PI * (f + 0.7790572732640 + 0.00273781191135448 * t));
        end

