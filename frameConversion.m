        function r = iauFw2m(obj, gamb, phib, psi, eps)
            % Construct the matrix.
            r = zeros(3);
            r = obj.iauIr(r);
            r = obj.iauRz(gamb, r);
            r = obj.iauRx(phib, r);
            r = obj.iauRz(-psi, r);
            r = obj.iauRx(-eps, r);
        end

        function gst = iauGst06(obj, uta, utb, tta, ttb, rnpb)
            % Extract CIP coordinates.
            [x, y] = obj.iauBpn2xy(rnpb);
            % The CIO locator, s.
            s = obj.iauS06(tta, ttb, x, y);
            % Greenwich apparent sidereal time.
            era = obj.iauEra00(uta, utb);
            eors = obj.iauEors(rnpb, s);
            gst = obj.iauAnp(era - eors);
        end

