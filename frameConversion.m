        end

        function sp = iauSp00(obj, date1, date2)
            % Interval between fundamental epoch J2000.0 and current date (JC).
            t = ((date1 - obj.constDJ00) + date2) / obj.constDJC;
            % Approximate s'.
            sp = -47e-6 * t * obj.constDAS2R;
        end
